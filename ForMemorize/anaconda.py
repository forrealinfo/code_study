# 가상환경명: testRepo

# 가상환경 생성
# conda create --name testRepo python=3.8.1

# 가상환경 전체 검색
# conda info --envs

# 가상환경 활성화
# activate testRepo

# 가상환경 비활성화
# deactivate testRepo


# pip 패키지 설치
# pip install simplejson

# pip 패키지 삭제
# pip uninstall simplejson

# 패키지 리스트 확인
# conda list

# 가상환경 삭제
# root 계정으로 활성화(activate)후 다른 가상환경을 삭제할것.
# conda remove --name testRepo --all

# 캐시삭제
# conda clean --all

# 아나콘다 버젼확인
# conda --version

# 아나콘다 업데이트
# conda update --name base conda

# 가상환경 패키지 업데이트
# conda update --all
